#include "libft.h"

char	*ft_strndup(const char *str, size_t n)
{
	size_t	i;
	char	*output;

	if (str == NULL)
		return (NULL);
	output = malloc(sizeof(char) * n + 1);
	if (output == NULL)
		return (NULL);
	i = -1;
	while (++i < n)
		output[i] = str[i];
	output[i] = '\0';
	return (output);
}